/**
3 COMMENT TYPES IN JAVA
 */
public class Comments {
    public static void main(String[] args) {
        
        // This is a single line comment.
        System.out.println("Single-line comment above this line.");

        /*
         * This is a multi-line comment.
         * It can be used for long comments.
         */
        System.out.println("Multi-line comment above this line.");

        /**
         This is a documentation comment. It's used to describe methods
         and classes in your code.
         */
        System.out.println("Documentation comment above this line.");
    }
}
